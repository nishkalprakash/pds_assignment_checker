#include<stdio.h>
int main()
{
  int m,n;
  printf("Enter 2 nos");
  scanf("%d%d",&m,&n);
  printf("The sum of m and n is:%d",m+n);
  printf("The difference of m and n is:%d",m-n);
  printf("The product of m and n is :%d",m*n);
  printf("The quotient of m and n is:%d",m/n);
  printf("The average of m and n is :%d",m+n/2);
  return 0;
}
