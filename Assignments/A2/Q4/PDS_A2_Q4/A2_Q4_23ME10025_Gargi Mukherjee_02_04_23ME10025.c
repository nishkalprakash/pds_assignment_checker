#include <stdio.h>

int main()
{
  int m,n;
  printf("Enter the value of m:");
  scanf("%d",&m);
  printf("Enter the value of n:");
  scanf("%d",&n);
  
  printf("The sum=%d \n",m+n);
  
  printf("The difference=%d \n",m-n);
  
  printf("The product=%d \n",m*n);
  
  printf("The integer quotuent=%d \n",m/n);
  printf("The integer remainder=%d \n",m%n);

  printf("The floating point quotient=%f \n", (float)m/(float)n);
  
  printf("The sum of the squares=%d \n",(m*m+n*n));
  printf("The square of sum=%d \n",(m+n)*(m+n));
 
  printf("The average=%f \n",(float)(m+n)/2);
 
  printf("The average of the sum of the squares=%f \n",(float)(m*m+n*n)/2);
  
  return 0;
}
	 
    
	 
		
  
  
  
