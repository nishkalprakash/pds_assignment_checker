#include<stdio.h>
int main()
{
  int m ,n;
  printf("Enter two numbers \n");
  scanf("%d %d",&m,&n);
  printf("The sum of m and n is %d\n", m+n);
  printf("The difference of m and n is %d\n", m-n);
  printf("The product of m and n is %d\n", m*n);
  printf("The integer quotient of m divided by n is %d\n", m/n);
  printf("The integer remainder of m divided by n is %d\n",m%n);
  printf("The floating point quotient of m divided by n is %f\n", (float) m/n);
  printf("The sum of squares of m and n is %d\n", m*m + n*n);
  printf("The square of the sum of m and n is %d\n", (m+n)*(m+n));
  printf("The floating point average of m and is %f\n", (m+n)/2.0);
  printf("The average of sum of sqaures of m and n is %f\n", (m*m + n*n)/2.0);
  return 0;
}
						      
 
