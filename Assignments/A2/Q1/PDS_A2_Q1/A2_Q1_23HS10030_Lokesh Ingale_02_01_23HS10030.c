#include<stdio.h>
int main()
{
  int Integral;
  float Number,Fraction;
  printf("Enter the Floating point number");
  scanf("%f",&Number);
  Integral=Number;
  Fraction=Number-Integral;
  printf("Integral part=%d \n",Integral);
  printf("Fractional part=%f \n",Fraction);
  return 0;
}
    
